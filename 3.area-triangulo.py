'''
Calcular el área de un triángulo
1. Crear variable con int para que base y altura.
2. Crear variable área que haga la operación aritmática.
2. Imprimir el mensaje con el resultado.
'''

base = int(input("Escribe la base del triángulo: "))
altura = int(input("Escribe la altura del triángulo: "))
area = (base * altura) / 2

print("El área del triángulo es:", area, "cm².")
